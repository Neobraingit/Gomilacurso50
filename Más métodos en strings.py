s = 'Mallorca es una isla preciosa'
print (s.startswith('m')) # Nos devolverá verdadero si empieza con el caracter dado
print (s.startswith('Mallorca')) # Hace lo mismo que el anterior
print (s.endswith('a')) # Nos devuelve un valor booleano si cumple el parámetro dado
print (s.isalnum()) # Nos devuelve True si es alfanumérico
print (s.isalpha()) # Nos devuelve True si pertenece al alfabeto
print (s.isdigit()) # Si son dígitos nos devolverá True
print (s.isspace()) # Si son espacios en blanco
print (s.islower()) # Si están en minñúsculas
print (s.upper()) # Lo contrario al anterior
print (s.istitle()) # Si todas las palabras empiezan en mayúsculas
